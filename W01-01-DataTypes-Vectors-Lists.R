
x <- c(0.5, 0.6)       ## Numeric
print(x)

x <- c(TRUE, FALSE)    ## Logical
print(x)

x <- c(T, F)           ## Logical
print(x)

x <- c("a", "b", "c")  ## Character
print(x)

x <- 9:29              ## Integer
print(x)

x <- c(1+0i, 2+4i)     ## Complex
print(x)

